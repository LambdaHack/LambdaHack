// Runs a GHC wasm32-wasi *command* binary (built the normal way, with a
// plain `_start` -- not a reactor) that also needs GHC's JSFFI glue.
//
// `wasmtime` alone supplies the wasi_snapshot_preview1 import, but not the
// extra ghc_wasm_jsffi import that GHC's JS FFI needs; that import can only
// be satisfied by an actual JS host. This script provides one via Node,
// wiring things up the way the GHC User's Guide's wasm backend chapter
// describes: https://downloads.haskell.org/ghc/latest/docs/users_guide/wasm.html
//
// Usage: node run-wasm-test.mjs <foo.wasm> <ghc_wasm_jsffi.mjs> [args...]
//   <ghc_wasm_jsffi.mjs> is generated by post-link.mjs, e.g.:
//     ~/.ghc-wasm/wasm32-wasi-ghc/lib/post-link.mjs --input foo.wasm --output ghc_wasm_jsffi.mjs

import { existsSync, readFileSync } from 'node:fs';
import { basename, resolve } from 'node:path';
import { pathToFileURL } from 'node:url';
import { WASI } from 'node:wasi';

const [, , wasmPath, jsffiPath, ...extraArgs] = process.argv;

if (!wasmPath || !jsffiPath) {
  console.error(
    'usage: node run-wasm-test.mjs <foo.wasm> <ghc_wasm_jsffi.mjs> [args...]'
  );
  process.exit(2);
}
for (const p of [wasmPath, jsffiPath]) {
  if (!existsSync(p)) {
    console.error(`run-wasm-test.mjs: no such file: ${p}`);
    process.exit(2);
  }
}

const wasi = new WASI({
  version: 'preview1',
  // Haskell's getArgs excludes argv[0], so this only affects things that
  // read the program name directly (getProgName, --help banners); use a
  // short name instead of the full build-tree path for those.
  args: [basename(wasmPath), ...extraArgs],
  env: process.env,
  // GHC's RTS init (hs_init_ghc) does a chdir to an absolute host path
  // during startup (seemingly derived from PWD/argv0), and WASI only lets
  // that resolve through a preopened directory. wasmtime doesn't grant
  // filesystem access by default either (its own docs show `wasmtime run
  // --mapdir /::$PWD` is needed for RTS features that touch the
  // filesystem) -- this requirement was just masked until now by the
  // JSFFI instantiation failure happening first. This only ever runs a
  // locally built test binary, not untrusted code, so preopening
  // everything is simpler than guessing exactly which path the RTS wants.
  preopens: { '/': '/' },
  returnOnExit: true,
});

// The generated glue needs the instantiated module's own exports (mainly
// `memory`) to implement ghc_wasm_jsffi, but imports have to be supplied
// before an instance exists. Tie the knot with a plain object that gets
// filled in right after instantiation.
const exportsPlaceholder = {};
const { default: makeJsffiImports } = await import(
  pathToFileURL(resolve(jsffiPath)).href
);

const wasmBuffer = readFileSync(wasmPath);
const { instance } = await WebAssembly.instantiate(wasmBuffer, {
  wasi_snapshot_preview1: wasi.wasiImport,
  ghc_wasm_jsffi: makeJsffiImports(exportsPlaceholder),
});
Object.assign(exportsPlaceholder, instance.exports);

// The test-suite is a plain exitcode-stdio-1.0 executable (a WASI command,
// exporting `_start`), not a reactor, so run it the same way wasmtime would
// and propagate its exit code -- `make` needs this to notice test failures.
process.exitCode = wasi.start(instance);
